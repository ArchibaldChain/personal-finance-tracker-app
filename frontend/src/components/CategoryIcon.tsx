/** All Lucide icon names available in this app */
export const ALL_ICON_NAMES = Object.keys(ICON_MAP).sort();

interface CategoryIconProps {
  name: string | null | undefined;
  size?: number;
  color?: string;
  strokeWidth?: number;
}

/** Renders a Lucide icon by string name. Falls back to CircleEllipsis if unknown. */
export default function CategoryIcon({ name, size = 16, color, strokeWidth = 1.75 }: CategoryIconProps) {
  const Icon = (name && ICON_MAP[name]) ? ICON_MAP[name] : CircleEllipsis;
  return <Icon size={size} color={color} strokeWidth={strokeWidth} />;
}
